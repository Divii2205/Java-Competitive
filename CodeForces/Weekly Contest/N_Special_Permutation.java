import java.util.*;

public class N_Special_Permutation {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int t = sc.nextInt();

        for (int i = 1; i <=t; i++){
            int n = sc.nextInt();

            for (int j = 2; j <=n; j++){
                System.out.print(j + " ");
            } System.out.println(1);
        }
        sc.close();
    }    
}